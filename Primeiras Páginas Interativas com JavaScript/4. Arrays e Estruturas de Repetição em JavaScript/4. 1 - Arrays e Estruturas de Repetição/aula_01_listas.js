// Lista

const alunos = ['João', 'Vitor', 'Marina']

console.log(alunos);

// Acessar posição especifica da lista

console.log(alunos[0]);

// Adicionar item na lista

alunos.push('Diego');

console.log(alunos);

//Ou

alunos[2] = 'Livia'

console.log(alunos);

// Remover item da lista(remove o último item da lista)

alunos.pop();

console.log(alunos);

// Remover item da lista(remove o primeiro item da lista)

alunos.shift();

console.log(alunos);

// Mostrar tamanho da lista

console.log(alunos.length)


